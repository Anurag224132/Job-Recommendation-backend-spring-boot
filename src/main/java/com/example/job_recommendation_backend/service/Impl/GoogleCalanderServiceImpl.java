package com.example.job_recommendation_backend.service.Impl;

import com.example.job_recommendation_backend.service.GoogleCalendarService;
import org.springframework.beans.factory.annotation.Value;
import org.springframework.http.HttpEntity;
import org.springframework.http.HttpHeaders;
import org.springframework.http.MediaType;
import org.springframework.http.ResponseEntity;
import org.springframework.stereotype.Service;
import org.springframework.util.LinkedMultiValueMap;
import org.springframework.util.MultiValueMap;
import org.springframework.web.client.RestTemplate;

import java.time.LocalDateTime;
import java.time.ZoneId;
import java.time.format.DateTimeFormatter;
import java.util.HashMap;
import java.util.List;
import java.util.Map;
import java.util.UUID;
import org.springframework.web.client.HttpClientErrorException;

@Service
public class GoogleCalanderServiceImpl implements GoogleCalendarService {

    @Value("${google.client.id}")
    private String clientId;

    @Value("${google.client.secret}")
    private String clientSecret;

    @Value("${google.refresh.token}")
    private String refreshToken;


    private final RestTemplate restTemplate;

    public GoogleCalanderServiceImpl(RestTemplate restTemplate) {
        this.restTemplate = restTemplate;
    }

    @Override
    public String createMeetLink(LocalDateTime startTime, String candidateEmail) {

        String accessToken = getAccessToken();

        String url = "https://www.googleapis.com/calendar/v3/calendars/primary/events?conferenceDataVersion=1";

        HttpHeaders headers = new HttpHeaders();
        headers.setBearerAuth(accessToken);
        headers.setContentType(MediaType.APPLICATION_JSON);

        String startIso = startTime.withNano(0)
                .atZone(ZoneId.of("Asia/Kolkata"))
                .withZoneSameInstant(ZoneId.of("UTC"))
                .format(DateTimeFormatter.ISO_INSTANT);

        String endIso = startTime.withNano(0).plusHours(1)
                .atZone(ZoneId.of("Asia/Kolkata"))
                .withZoneSameInstant(ZoneId.of("UTC"))
                .format(DateTimeFormatter.ISO_INSTANT);

        Map<String, Object> body = new HashMap<>();

        body.put("summary", "Technical Interview");
        body.put("description", "Interview with " + candidateEmail);

        body.put("start", Map.of("dateTime", startIso));
        body.put("end", Map.of("dateTime", endIso));

        body.put("conferenceData", Map.of(
                "createRequest", Map.of(
                        "requestId", UUID.randomUUID().toString(),
                        "conferenceSolutionKey", Map.of(
                                "type", "hangoutsMeet"
                        )
                )
        ));

        HttpEntity<Map<String, Object>> request = new HttpEntity<>(body, headers);

        try {
            ResponseEntity<Map> response =
                    restTemplate.postForEntity(url, request, Map.class);

            if (response.getBody() == null) {
                throw new RuntimeException("Empty response from Google API");
            }

            Map<String, Object> responseBody = response.getBody();

            Map<String, Object> conferenceData =
                    (Map<String, Object>) responseBody.get("conferenceData");

            if (conferenceData == null) {
                throw new RuntimeException("No conference data returned from Google Calendar");
            }

            List<Map<String, Object>> entryPoints =
                    (List<Map<String, Object>>) conferenceData.get("entryPoints");

            if (entryPoints == null || entryPoints.isEmpty()) {
                throw new RuntimeException("No meeting link generated by Google Meet");
            }

            return entryPoints.stream()
                    .filter(ep -> "video".equals(ep.get("entryPointType")))
                    .map(ep -> (String) ep.get("uri"))
                    .findFirst()
                    .orElse((String) entryPoints.get(0).get("uri"));

        } catch (HttpClientErrorException e) {
            System.err.println("❌ Google API Error Response: " + e.getResponseBodyAsString());
            throw new RuntimeException("Google Calendar API failed: " + e.getResponseBodyAsString(), e);
        }
    }

    private String getAccessToken() {

        String url = "https://oauth2.googleapis.com/token";

        HttpHeaders headers = new HttpHeaders();
        headers.setContentType(MediaType.APPLICATION_FORM_URLENCODED);

        MultiValueMap<String, String> body = new LinkedMultiValueMap<>();
        body.add("client_id", clientId);
        body.add("client_secret", clientSecret);
        body.add("refresh_token", refreshToken);
        body.add("grant_type", "refresh_token");

        HttpEntity<MultiValueMap<String, String>> request =
                new HttpEntity<>(body, headers);

        ResponseEntity<Map> response =
                restTemplate.postForEntity(url, request, Map.class);

        if (response.getBody() == null || response.getBody().get("access_token") == null) {
            throw new RuntimeException("Failed to get access token");
        }

        return (String) response.getBody().get("access_token");
    }
}
